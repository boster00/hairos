/** Top earning quests for RewardsPanel (by rewardCredits, exclude locked). */
export function getTopEarningQuests(quests, limit = 3) {
  return [...quests]
    .filter((q) => q.status !== "locked")
    .sort((a, b) => (b.rewardCredits || 0) - (a.rewardCredits || 0))
    .slice(0, limit);
}
